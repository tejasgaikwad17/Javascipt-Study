// String to Number
let age = "25";
console.log(Number(age));

// Invalid String
console.log(Number("25abc"));

// Boolean to Number
console.log(Number(true));
console.log(Number(false));

// Null
console.log(Number(null));

// Undefined
console.log(Number(undefined));

// Empty String
console.log(Number(""));

// Number to String
console.log(String(100));

// Number to Boolean
console.log(Boolean(0));
console.log(Boolean(50));

// String to Boolean
console.log(Boolean(""));
console.log(Boolean("Hello"));

// Implicit Conversion
console.log(10 + "20");
console.log("10" + 20 + 30);
console.log(10 + 20 + "30");
console.log("10" - 5);
console.log("10" * 2);
console.log("20" / 2);


